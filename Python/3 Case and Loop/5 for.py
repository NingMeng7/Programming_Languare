# 一个基本类型的迭代
for n in range(1, 11):          # range左闭右开, [1,11)
    print(n)
input('waiting')
# range(start, end, step)

# list 迭代
names = ['Michael', 'Bob', 'Tracy']                 # list 也可以
for name in names:
	print(name)
input('waiting')

# tuple 迭代
names = ('Michael', 'Bob', 'Tracy')                 # tuple 也可以
for name in names:
  print(name)
input('waiting')


